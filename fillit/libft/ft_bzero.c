#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t		i;
	char		*ptr;

	i = 0;
	ptr = str;
	if (n > 0)
	{
		while (i < n)
		{
			ptr[i] = '\0';
			i++;
		}
	}
}
