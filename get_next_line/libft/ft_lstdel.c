#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*ptr;
	t_list	*tmp;

	ptr = *alst;
	while (ptr)
	{
		tmp = ptr->next;
		del(ptr->content, ptr->content_size);
		free(ptr);
		ptr = tmp;
	}
	*alst = NULL;
}
