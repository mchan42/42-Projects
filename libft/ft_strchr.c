#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	*string;

	i = 0;
	string = (char *)str;
	while (str[i] != '\0')
	{
		if (str[i] == (char)c)
			return ((char *)(str + i));
		i++;
	}
	if (str[i] == (char)c)
		return ((char *)(str + i));
	return (NULL);
}
